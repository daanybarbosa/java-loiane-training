package com.loiane.basico;

/**
 * Aula 5 - Hello World
 *
 * - Nome da classe segue a regra CamelCase: HelloWorld
 * - Java é camelSensitive
 *
 * - class - declaração da Classe
 * - HelloWorld - nome da Classe
 * - public static void main(String[] args){...} - método
 * - public static - modificadores de acesso do método
 * - void - tipo de retorno do método - void não retorna nada
 * - main - nome do método
 * - String[] args - parametros/argumentos do método
 * - { ... } - nosso códido (dentro de chaves)
 * - {} - blocos estão entre {} (chaves)
 */

public class Aula01_HelloWorld {

    public static void main(String[] args) {
        System.out.println("Hello World!"); //output na tela
    }
}
